/**
 * Add two numbers, and return the result.
 *
 * @param {Number} a
 * @param {Number} b
 * @return {Number}
 */
function add(a, b) {
  return a + b
}

/**
 * Reverse a string, and return the result.
 *
 * @param {String} input
 * @return {String}
 */
function reverse(input) {
  return [...input].reverse()
}
